# Lists store multiple values in a single variable.
# Lists are ordered, mutable, and allow duplicates.
# Various methods exist to modify, sort, and loop through lists.

# Method	Description
# append(x)	Adds x to the end
# insert(i, x)	Inserts x at index i
# remove(x)	Removes first occurrence of x
# pop(i)	Removes element at index i
# clear()	Removes all elements
# sort()	Sorts the list
# reverse()	Reverses the list
# index(x)	Returns index of x
# count(x)	Returns count of x
# copy()	Returns a copy of the list
# extend(iterable)	Extends list with elements from iterable