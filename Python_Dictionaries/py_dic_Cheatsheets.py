## **9. Dictionary Methods Cheat Sheet**
# | Method | Description |
# |--------|-------------|
# | `dict()` | Creates a dictionary |
# | `get(key, default)` | Returns the value for a key, or default if not found |
# | `keys()` | Returns a list of dictionary keys |
# | `values()` | Returns a list of dictionary values |
# | `items()` | Returns a list of (key, value) pairs |
# | `update(dict2)` | Merges another dictionary |
# | `pop(key)` | Removes a key and returns its value |
# | `popitem()` | Removes and returns the last key-value pair |
# | `del dict[key]` | Deletes a specific key |
# | `clear()` | Removes all elements |
# | `copy()` | Returns a shallow copy |
